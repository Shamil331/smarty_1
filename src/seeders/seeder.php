        $posts = array_merge($posts, $this->generateSportsPosts());
        $posts = array_merge($posts, $this->generateBusinessPosts());
        $posts = array_merge($posts, $this->generateMoviePosts());
        $posts = array_merge($posts, $this->generateMarketingPosts());
        
        shuffle($posts);
        
        $this->posts = [];
        $startDate = strtotime('-6 months');
        
        foreach ($posts as $index => $post) {
            $randomDays = rand(0, 180);
            $createdAt = date('Y-m-d H:i:s', $startDate + ($randomDays * 86400));
            
            $views = rand(0, 5000);
            
            $stmt = $this->db->prepare("
                INSERT INTO posts (title, description, content, image, views, created_at) 
                VALUES (?, ?, ?, ?, ?, ?)
            ");
            
            $image = $this->getRandomImage($post['category'][0]);
            
            $stmt->execute([
                $post['title'],
                $post['description'],
                $post['content'],
                $image,
                $views,
                $createdAt
            ]);
            
            $postId = $this->db->lastInsertId();
            $this->posts[] = [
                'id' => $postId,
                'categories' => $post['category']
            ];
        }
    }
    
    private function createPostCategories() {        
        $count = 0;
        foreach ($this->posts as $post) {
            foreach ($post['categories'] as $categoryIndex) {
                $stmt = $this->db->prepare("INSERT INTO post_categories (post_id, category_id) VALUES (?, ?)");
                $stmt->execute([$post['id'], $this->categoryIds[$categoryIndex]]);
                $count++;
            }
        }
    }
    
